package com.stylight.assessment.model;

public class Dummy {
	String param;
	boolean find;

	public Dummy() {
		super();
	}

	public Dummy(String param, boolean find) {
		super();
		this.param = param;
		this.find = find;
	}

	public String getParam() {
		return param;
	}

	public void setParam(String param) {
		this.param = param;
	}

	public boolean isFind() {
		return find;
	}

	public void setFind(boolean find) {
		this.find = find;
	}
}
